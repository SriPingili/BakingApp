/**
 * UI test for the MainActivity
 *
 * Used the Udacity classroom videos on UI testing as reference
 */
@RunWith(AndroidJUnit4.class)
public class MainActivityUITest {
    @Rule
    public IntentsTestRule<MainActivity> mActivityRule = new IntentsTestRule<>(
            MainActivity.class);

    @Before
    public void stubAllExternalIntents() {
        intending(not(isInternal())).respondWith(new Instrumentation.ActivityResult(Activity.RESULT_OK, null));
    }

    /**
     * tests clicking on the recipe name in recylerview fires an intent
     * to the RecipeDetailActivity
     */
    @Test
    public void testFirstElementonBakingAppGridView() {
        onView(ViewMatchers.withId(R.id.recipe_names_recycler_view_id))
                .perform(RecyclerViewActions.actionOnItemAtPosition(1, click()));
        intended(allOf(hasComponent(RecipeDetailViewActivity.class.getName()), hasExtra(Intent.EXTRA_TEXT, MainActivity.recipeData.get(1))));
    }
}
